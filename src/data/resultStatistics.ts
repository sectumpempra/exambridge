export interface YearlyStats {
  year: number;
  series: "june" | "november" | "march" | "summer" | "january" | "autumn";
  aStarRate: number;
  aRate: number;
  bRate: number;
  cRate: number;
  dRate: number;
  eRate: number;
  entries?: number;
}

export interface SubjectStats {
  code: string;
  name: string;
  board: string;
  level: "A-Level" | "AS-Level" | "IGCSE" | "GCSE";
  years: YearlyStats[];
}

function y(year: number, series: "june" | "november" | "march" | "summer" | "january" | "autumn",
  aStar: number, a: number, b: number, c: number, d: number, e: number, entries?: number): YearlyStats {
  return { year, series, aStarRate: aStar, aRate: a, bRate: b, cRate: c, dRate: d, eRate: e, entries };
}

function s(code: string, name: string, board: string, level: "A-Level" | "AS-Level" | "IGCSE" | "GCSE", years: YearlyStats[]): SubjectStats {
  return { code, name, board, level, years };
}


