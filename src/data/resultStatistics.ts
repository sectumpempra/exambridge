/**
 * Get subjects sorted by priority:
 * 1. Pure Mathematics first (not Further)
 * 2. Further Mathematics
 * 3. Physics
 * 4. Economics
 * 5. Biology
 * 6. Chemistry
 * 7. Everything else — sorted by code
 */
export function getSortedSubjects(
  board: string,
  level: string
): { code: string; name: string }[] {
  const subjects = ALL_SUBJECT_STATS
    .filter((s) => s.board === board && s.level === level);

  const getPriority = (name: string): number => {
    const lower = name.toLowerCase();
    // Group 1: Pure Mathematics / Maths (not Further)
    if ((lower.includes("mathematics") || lower.includes("maths")) 
        && !lower.includes("further")) return 1;
    // Group 2: Further Mathematics
    if (lower.includes("further mathematics") || lower.includes("further math") 
        || lower.includes("further maths")) return 2;
    // Group 3: Physics
    if (lower.includes("physics")) return 3;
    // Group 4: Economics
    if (lower.includes("economics")) return 4;
    // Group 5: Biology
    if (lower.includes("biology")) return 5;
    // Group 6: Chemistry
    if (lower.includes("chemistry")) return 6;
    // Group 7: Everything else
    return 7;
  };

  return subjects
    .sort((a, b) => {
      const pa = getPriority(a.name);
      const pb = getPriority(b.name);
      if (pa !== pb) return pa - pb;
      return a.code.localeCompare(b.code, undefined, { numeric: true });
    })
    .map((s) => ({ code: s.code, name: s.name }));
}

/** Get all subjects for a given board and level (sorted) */
export function getAvailableSubjects(
  board: string,
  level: string
): { code: string; name: string }[] {
  return getSortedSubjects(board, level);
}

/** Get stats by subject code and board */
export function getSubjectStats(code: string, board: string): SubjectStats | undefined {
  return ALL_SUBJECT_STATS.find(
    (s) => s.code === code && s.board === board
  );
}

/** Get stats by board and level */
export function getStatsByBoardAndLevel(
  board: string,
  level: string
): SubjectStats[] {
  return ALL_SUBJECT_STATS.filter(
    (s) => s.board === board && s.level === level
  );
}

/** Get all unique boards */
export function getAvailableBoards(): string[] {
  return [...new Set(ALL_SUBJECT_STATS.map((s) => s.board))];
}

/** Get all levels available for a board */
export function getAvailableLevels(board: string): string[] {
  return [...new Set(
    ALL_SUBJECT_STATS.filter((s) => s.board === board).map((s) => s.level)
  )];
}
