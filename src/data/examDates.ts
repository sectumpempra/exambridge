export type SubjectCategory = "math" | "physics" | "chemistry" | "economics" | "biology" | "cs" | "other";

export const CATEGORY_NAMES: Record<SubjectCategory, string> = {
  math: "数学",
  physics: "物理",
  chemistry: "化学",
  economics: "经济",
  biology: "生物",
  cs: "计算机",
  other: "其他",
};

/** Classify subject code into category */
export function getSubjectCategory(code: string): SubjectCategory {
  const c = code.toLowerCase();

  // CAIE A-Level
  if (c === "9709" || c === "9231") return "math";
  if (c === "9702") return "physics";
  if (c === "9701") return "chemistry";
  if (c === "9700") return "biology";
  if (c === "9708") return "economics";
  if (c === "9608" || c === "9618") return "cs";

  // CAIE GCSE / IGCSE
  if (c === "0580" || c === "0606") return "math";
  if (c === "0625") return "physics";
  if (c === "0620") return "chemistry";
  if (c === "0610") return "biology";
  if (c === "0455") return "economics";
  if (c === "0478") return "cs";

  // Edexcel A-Level IAL
  if (c.startsWith("wma") || c.startsWith("wfm") || c.startsWith("wme") || c.startsWith("wst") || c.startsWith("wdm")) return "math";
  if (c.startsWith("wph")) return "physics";
  if (c.startsWith("wch")) return "chemistry";
  if (c.startsWith("wbi")) return "biology";
  if (c.startsWith("wec")) return "economics";

  // Edexcel GCSE
  if (c.startsWith("4ma") || c.startsWith("4pm") || c.startsWith("1ma")) return "math";
  if (c === "1en0") return "other";

  // AQA GCSE
  if (c.startsWith("8300") || c.startsWith("8365")) return "math";
  if (c.startsWith("8461")) return "biology";
  if (c.startsWith("8462")) return "chemistry";
  if (c.startsWith("8463")) return "physics";
  if (c.startsWith("8700") || c.startsWith("8702")) return "other";

  // OCR GCSE
  if (c.startsWith("j560")) return "math";
  if (c.startsWith("j257")) return "biology";
  if (c.startsWith("j258")) return "chemistry";
  if (c.startsWith("j259")) return "physics";
  if (c.startsWith("6993")) return "math";
  if (c.startsWith("j351") || c.startsWith("j352")) return "other";

  // OCR A-Level
  if (c.startsWith("h240") || c.startsWith("h245")) return "math";
  if (c.startsWith("h556") || c.startsWith("h557")) return "physics";
  if (c.startsWith("h420")) return "biology";
  if (c.startsWith("h432") || c.startsWith("h422")) return "chemistry";
  if (c.startsWith("h446") || c.startsWith("h443")) return "cs";
  if (c.startsWith("h481")) return "other";
  if (c.startsWith("h567")) return "other";
  if (c.startsWith("h407") || c.startsWith("h408")) return "other";
  if (c.startsWith("h472")) return "other";
  if (c.startsWith("h573")) return "other";
  if (c.startsWith("h543")) return "other";
  if (c.startsWith("h415") || c.startsWith("h418")) return "other";
  if (c.startsWith("h580")) return "other";
  if (c.startsWith("h431")) return "biology";
  if (c.startsWith("h433")) return "chemistry";

  // AQA A-Level
  if (c.startsWith("ma0")) return "math";
  if (c.startsWith("bl0")) return "biology";
  if (c.startsWith("ch0")) return "chemistry";
  if (c.startsWith("ph0")) return "physics";
  if (c.startsWith("ec0")) return "economics";
  if (c.startsWith("cs0")) return "cs";
  if (c.startsWith("fm0")) return "math";

  return "other";
}
